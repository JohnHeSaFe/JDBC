    private void listEmployees() {
        employeesListTextArea.setText(""); // limpiar antes de escribir

        if (EmployeeManager.getListEmployees().isEmpty()) {
            employeesListTextArea.append("No employees found.\n");
            return;
        }

        for (Employee employee : EmployeeManager.getListEmployees().values()) {
            employeesListTextArea.append("ID: " + employee.getId() + "\n");
            employeesListTextArea.append("Name: " + employee.getName() + "\n");
            employeesListTextArea.append("Age: " + employee.getAge() + "\n");
            employeesListTextArea.append("Department: " + employee.getDepartment() + "\n");
            employeesListTextArea.append("Salary: " + String.format("%.2f", employee.getSalary()) + " â‚¬" + "\n");
            employeesListTextArea.append("=============================\n");
        }
    }

